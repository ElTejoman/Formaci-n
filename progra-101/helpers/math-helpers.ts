export function addTwoNumbers(num1: number, num2: number) {
  return num1 + num2;
   
}

export function subTwoNumbers(num1: number, num2: number) {
  return num1 -num2;
   
}

export function timesTwoNumbers(num1: number, num2: number) {
  return num1 * num2;
   
}

export function divTwoNumbers(num1: number, num2: number) {
  return num1 / num2;
   
}